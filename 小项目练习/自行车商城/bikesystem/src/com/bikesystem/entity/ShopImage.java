package com.bikesystem.entity;

public class ShopImage {
	private int siid;
	private String shopname;
	private String imageName;
	private String imagePath;
	
	public ShopImage() {
		super();
	}
	

	public ShopImage(String shopname, String imageName, String imagePath) {
		super();
		this.shopname = shopname;
		this.imageName = imageName;
		this.imagePath = imagePath;
	}


	public ShopImage(int siid, String shopname, String imageName, String imagePath) {
		super();
		this.siid = siid;
		this.shopname = shopname;
		this.imageName = imageName;
		this.imagePath = imagePath;
	}


	public int getSiid() {
		return siid;
	}


	public void setSiid(int siid) {
		this.siid = siid;
	}

	public String getShopname() {
		return shopname;
	}

	public void setShopname(String shopname) {
		this.shopname = shopname;
	}


	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}


	@Override
	public String toString() {
		return "ShopImage [siid=" + siid + ", shopname=" + shopname + ", imageName=" + imageName + ", imagePath="
				+ imagePath + "]";
	}
	
}
