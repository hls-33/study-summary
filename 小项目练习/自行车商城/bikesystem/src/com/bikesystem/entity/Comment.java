package com.bikesystem.entity;

public class Comment {
	private int cid;
	private String name;	//引用user表
	private String shopName;
	private String bikeName;//关联bike_sell或bike_rent表
	private String message;
	private String commentDate;//评论日期
	private String commentStatus;//评论好中差
	private String commentImgPath;//评论图片路径
	
	public Comment() {
		super();
	}
	
	public Comment(String message) {
		super();
		this.message = message;
	}

	public Comment(int cid, String name, String shopName, String bikeName, String message, String commentDate,
			String commentStatus, String commentImgPath) {
		super();
		this.cid = cid;
		this.name = name;
		this.shopName = shopName;
		this.bikeName = bikeName;
		this.message = message;
		this.commentDate = commentDate;
		this.commentStatus = commentStatus;
		this.commentImgPath = commentImgPath;
	}

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public String getBikeName() {
		return bikeName;
	}

	public void setBikeName(String bikeName) {
		this.bikeName = bikeName;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getCommentDate() {
		return commentDate;
	}

	public void setCommentDate(String commentDate) {
		this.commentDate = commentDate;
	}

	public String getCommentStatus() {
		return commentStatus;
	}

	public void setCommentStatus(String commentStatus) {
		this.commentStatus = commentStatus;
	}

	public String getCommentImgPath() {
		return commentImgPath;
	}

	public void setCommentImgPath(String commentImgPath) {
		this.commentImgPath = commentImgPath;
	}
	
	
	
	
	
}
