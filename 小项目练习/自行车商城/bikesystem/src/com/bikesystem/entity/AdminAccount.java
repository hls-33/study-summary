package com.bikesystem.entity;
/**
 * 管理员账目表：admin_account对应的admin收支bean类
 * @author bwfadmin
 *
 */
public class AdminAccount {
	private int acid;
	private int aid;
	private String aphone;
	private double income;
	private double spending;
	
	
	public AdminAccount() {
		super();
	}
	public AdminAccount(int acid, int aid, String aphone, double income, double spending) {
		super();
		this.acid = acid;
		this.aid = aid;
		this.aphone = aphone;
		this.income = income;
		this.spending = spending;
	}
	
	
	
	public AdminAccount(int aid, String aphone, double income, double spending) {
		super();
		this.aid = aid;
		this.aphone = aphone;
		this.income = income;
		this.spending = spending;
	}
	
	public int getAcid() {
		return acid;
	}
	public void setAcid(int acid) {
		this.acid = acid;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getAphone() {
		return aphone;
	}
	public void setAphone(String aphone) {
		this.aphone = aphone;
	}
	public double getIncome() {
		return income;
	}
	public void setIncome(double income) {
		this.income = income;
	}
	public double getSpending() {
		return spending;
	}
	public void setSpending(double spending) {
		this.spending = spending;
	}
	@Override
	public String toString() {
		return "AdminAccount [acid=" + acid + ", aid=" + aid + ", aphone=" + aphone + ", income=" + income
				+ ", spending=" + spending + "]";
	}
	
}
